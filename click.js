//declare samecolor() here
function samecolor(){
  let newcolor = "#000000";
  let divs = document.querySelectorAll("#samecolor div");

  for(i=0;i<divs.length;i++){
    document.divs[i].style.backgroundColor = newcolor;
    document.divs[i].style.height = "500px"
    document.divs[i].style.opacity = Math.random();
  }
}
